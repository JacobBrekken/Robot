void turnright(int degrees10)
{
	SensorValue[Gyro] = 0;
	int error = 5;

	//While the absolute value of the gyro is less than the desired rotation - 100...
	while(abs(SensorValue[in3]) < degrees10 - 100)
	{
		motor[rightdrive] = motor[rightmiddledrive] = -75;
		motor[frontleftdrive] = motor[backleftdrive] = motor[middleleftdrive] = 75;
	}
	//Brief brake to eliminate some drift
	motor[rightdrive] = motor[rightmiddledrive] = 5;
	motor[frontleftdrive] = motor[backleftdrive] = motor[middleleftdrive] = -5;
	wait1Msec(100);

	//Second while loop to move the robot more slowly to its goal, also setting up a range
	//for the amount of acceptable error in the system
	while(abs(SensorValue[in3]) > degrees10 + error || abs(SensorValue[in3]) < degrees10 - error)
	{
		if(abs(SensorValue[in3]) > degrees10)
		{
			motor[rightdrive] = motor[rightmiddledrive] = 30;
			motor[frontleftdrive] = motor[backleftdrive] = motor[middleleftdrive] = -30;
		}
		else
		{
			motor[rightdrive] = motor[rightmiddledrive] = -30;
			motor[frontleftdrive] = motor[backleftdrive] = motor[middleleftdrive] = 30;
		}
	}
	//Stop
	motor[rightdrive] = motor[rightmiddledrive] = 0;
	motor[frontleftdrive] = motor[backleftdrive] = motor[middleleftdrive] = 0;
	wait1Msec(250);
}

