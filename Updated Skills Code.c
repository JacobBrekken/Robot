task usercontrol()
{
	// User control code here, inside the loop

  while (true)
  {
  	startTask(pinch);
    motor[rightdrive] = motor[rightmiddledrive] = vexRT[Ch3] - (vexRT[Ch1]/1.2);
		motor[frontleftdrive] = motor[middleleftdrive] = motor[backleftdrive] = vexRT[Ch3] + (vexRT[Ch1]/1.2);
		if(vexRT[Btn6U] == 1)
		{
			if(SensorValue[ArmPot] > 400)
			{
				motor[leftarm] = motor[rightarm] = 127;
			}
			else
			{
				motor[leftarm] = motor[rightarm] = -15;
			}
		}
		else if(vexRT[Btn6D] == 1)
		{
			if(SensorValue[bumper] == 0)
			{
				motor[leftarm] = motor[rightarm] = -127;
			}
			else
			{
				motor[leftarm] = motor[rightarm] = -20;
			}
		}
		else
		{
			motor[leftarm] = motor[rightarm] = -20;
		}
		if(vexRT[Btn5U] == 1)
		{
			if(SensorValue[ClawPot] < 3610)
			{
				motor[claw] = 127;
			}
			else
			{
				motor[claw] = 0;
			}
		}
		else if(vexRT[Btn5D] == 1)
		{
			if(SensorValue[ClawPot] > 750)
			{
				motor[claw] = -127;
			}
			else
			{
				motor[claw] = 0;
			}
		}
		else if(SensorValue[ArmPot] < 1350)
		{
			if(SensorValue[ClawPot] > 2500)
			{
				activate = false;
				motor[claw] = -127;
			}
			else
			{
				motor[claw] = 0;
			}
		}
		else
		{
			motor[claw] = 0;
		}
		if(vexRT[Btn8R])
			activate = true;
		if(vexRT[Btn8L])
			activate = false;
  }
}
