void moveforward(int degrees)
{
	SensorValue[backleftIME] = 0;
	SensorValue[backrightIME] = 0;
	while(SensorValue[backleftIME] < degrees)
	{
		motor[rightdrive] = motor[rightmiddledrive] = motor[frontleftdrive] = motor[middleleftdrive] = motor[backleftdrive] = 127;
	}
	motor[rightdrive] = motor[rightmiddledrive] = motor[frontleftdrive] = motor[middleleftdrive] = motor[backleftdrive] = 0;
	wait1Msec(100);
}

void movebackward(int degrees)
{
	SensorValue[backleftIME] = 0;
	SensorValue[backrightIME] = 0;
	while(SensorValue[backleftIME] > degrees)
	{
		motor[rightdrive] = motor[rightmiddledrive] = motor[frontleftdrive] = motor[middleleftdrive] = motor[backleftdrive] = -75;
	}
	motor[rightdrive] = motor[rightmiddledrive] = motor[frontleftdrive] = motor[middleleftdrive] = motor[backleftdrive] = 0;
	wait1Msec(100);
}

void raisearm(int degrees)
{
	while(SensorValue[ArmPot] > degrees)
	{
		motor[rightarm] = motor[leftarm] = 40;
	}
	motor[leftarm] = motor[rightarm] = -20;
}

void lowerarm(int degrees)
{
	while(SensorValue[ArmPot] < degrees)
	{
		motor[leftarm] = motor[rightarm] = -70;
	}
	motor[leftarm] = motor[rightarm] = 0;
}

void lowerarmtobottom()
{
	while(SensorValue[bumper] == 0)
	{
		motor[leftarm] = motor[rightarm] = -70;
	}
	motor[leftarm] = motor[rightarm] = -20;
}

void openclaw(int degrees)
{
	while(SensorValue[ClawPot] > degrees)
	{
		motor[claw] = -100;
	}
	motor[claw] = 0;
	wait1Msec(100);
}

void closeclaw(int degrees)
{
	while(SensorValue[ClawPot] < degrees)
	{
		motor[claw] = 127;
	}
	motor[claw] = -10;
	wait1Msec(100);
}

task pinch()
{
	while(activate)
	{
		motor[claw] = 127;
		wait1Msec(250);
		motor[claw] = 20;
		wait1Msec(250);
	}
}

void throw()
{
	while(SensorValue[ArmPot] > 400)
	{
		motor[leftarm] = motor[rightarm] = 127;
		if(SensorValue[ArmPot] < 1350)
		{
			if(SensorValue[ClawPot] > 2500)
			{
				motor[claw] = -127;
			}
			else
			{
				motor[claw] = 0;
			}
		}
	}
	motor[leftarm] = motor[rightarm] = -20;
	motor[claw] = 0;
}

